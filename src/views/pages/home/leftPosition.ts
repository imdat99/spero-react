export const iconPosition = (
    position: Position,
    line: string,
    speed: number
) => {
    const { height, top, left } = position;
    const iconHeight = height - 600 - top;
    const leftVal = leftValue[line as keyof LeftVal].find(
        (v) =>
            v.borderA - 108.5 + speed < iconHeight &&
      v.borderB - 108.5 + speed >= iconHeight
    )?.value;
    const style = {
        position: "fixed",
        visibility: leftVal ? "visible" : "hidden",
        zIndex: 1,
        top: height - 500 || "10px",
        left: left + (leftVal || 0),
        transition: "top 0.01s ease 0s",
    };
    return style;
};
